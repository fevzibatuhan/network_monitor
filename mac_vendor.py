class MacVendorLookup:
    """MAC adresinden üretici firma bilgisi bulan sınıf."""

    def __init__(self):
        self._mac_vendor_lib = None
        self._init_mac_vendor_lib()

    def _init_mac_vendor_lib(self):
        """mac-vendor-lookup kütüphanesini yükle."""
        try:
            from mac_vendor_lookup import MacLookup
            self._mac_vendor_lib = MacLookup()
        except ImportError:
            self._mac_vendor_lib = None
        except Exception:
            self._mac_vendor_lib = None

    def _normalize_mac(self, mac: str) -> str:
        """MAC adresini standart formata dönüştür (XX:XX:XX:XX:XX:XX)."""
        mac = mac.strip().upper()
        mac = re.sub(r'[^0-9A-F]', '', mac)
        if len(mac) == 12:
            return ':'.join(mac[i:i+2] for i in range(0, 12, 2))
        return mac

    def _get_prefix(self, mac: str) -> str:
        """MAC adresinden OUI prefix'ini al (ilk 3 oktet)."""
        normalized = self._normalize_mac(mac)
        parts = normalized.split(':')
        if len(parts) >= 3:
            return ':'.join(parts[:3])
        return normalized

    def lookup(self, mac: str) -> str:
        """MAC adresinden üretici firma bilgisini döndür."""
        if not mac or mac == "N/A":
            return "Bilinmiyor"

        prefix = self._get_prefix(mac)

        # Önce gömülü veritabanına bak
        if prefix in BUILTIN_OUI_DB:
            return BUILTIN_OUI_DB[prefix]

        # mac-vendor-lookup kütüphanesini dene
        if self._mac_vendor_lib:
            try:
                result = self._mac_vendor_lib.lookup(mac)
                if result:
                    return result
            except Exception:
                pass

        return "Bilinmiyor"

    def get_device_type(self, vendor: str) -> str:
        """Üretici firmasından cihaz türü tahmini yap."""
        if not vendor or vendor == "Bilinmiyor":
            return "Bilinmiyor"

        # Doğrudan eşleşme
        if vendor in VENDOR_DEVICE_TYPE_MAP:
            return VENDOR_DEVICE_TYPE_MAP[vendor]

        # Kısmi eşleşme
        vendor_lower = vendor.lower()
        for key, device_type in VENDOR_DEVICE_TYPE_MAP.items():
            if key.lower() in vendor_lower or vendor_lower in key.lower():
                return device_type

        # Anahtar kelime bazlı tahmin
        router_keywords = ["router", "networking", "networks", "communications", "telecom"]
        phone_keywords = ["mobile", "phone", "wireless", "cellular"]
        iot_keywords = ["iot", "smart", "home", "automation", "sensor"]
        printer_keywords = ["printer", "printing", "print", "canon", "epson", "brother", "xerox", "ricoh"]
        camera_keywords = ["camera", "hikvision", "dahua", "axis", "surveillance"]

        for kw in router_keywords:
            if kw in vendor_lower:
                return "Ağ Cihazı"
        for kw in phone_keywords:
            if kw in vendor_lower:
                return "Telefon"
        for kw in iot_keywords:
            if kw in vendor_lower:
                return "IoT Cihazı"
        for kw in printer_keywords:
            if kw in vendor_lower:
                return "Yazıcı"
        for kw in camera_keywords:
            if kw in vendor_lower:
                return "IP Kamera"

        return "Bilinmiyor"
